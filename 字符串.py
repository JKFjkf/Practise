str = 'jiaokaifeng'
print(str)           #输出字符串
print(str[0:-1])     #输出第一个到倒数第二个的所有字符
print(str[0])        #输出字符串第一个字符
print(str[2:5])      #输出从第三个开始到后的第五个字符
print(str[2:])       #输出从第三个开始的所有字符
print(str * 2)       #输出字符串两次，也可以写成print（2 * str)
print(str + "TEST")  #连接字符串
print('jiaoka\nifeng')
print(r'jiaoka\nifeng')

